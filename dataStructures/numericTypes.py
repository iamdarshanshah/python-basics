# Complex types
d = 3+5j
print(d)
print(type(d))

# Binary types (start with 0B)
e = 0B1010
print(e)
print(type(e))

# Octadecimal types (start with 0O)
e = 0O16
print(e)
print(type(e))

# Hexadecimal types (start with 0X)
e = 0XFF
print(e)
print(type(e))

# Bool
f = 8<9
print(f)
print(type(f))

