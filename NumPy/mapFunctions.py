from numpy import *

a1 = array([1, 2, 3, 4])
a2 = array([10, 11, 12, 13])

print(a1 + a2)  # [11 13 15 17]

print(a1 - 2)  # [-1  0  1  2]

print(sin(a1))  # [ 0.84147098  0.90929743  0.14112001 -0.7568025 ]

print(log(a2))  # [2.30258509 2.39789527 2.48490665 2.56494936]

print(mean(a1))  # 2.5

print(sqrt(a1))  # [1.         1.41421356 1.73205081 2.        ]

print(absolute(a1))  # [1 2 3 4]
